                      <h2 class="mt-0 mb-4">What are we doing?</h2> 
<p>Well... preflight. Pre-flighting in printing is the process of checking a design file for inefficiencies/errors and making sure itâ€™s set-up correctly before before sending it to print or final production. Preparation for printed jobs ranges according to the complexity of a project, but there are some fundamental things to check before send each print job to ensure that you are producing the intended outcome.</p> 

<p>Most DTP tools has a pre-flighting tools that checks errors for color mode, missing links, missing fonts, detects spot colors and overset text. But as in every process, the weakest link is human factor. Many graphic designers, illustrators or typesetter does not really check, sometimes they will miss something. In a word, printing problems are getting ready. There must be a team of specialists in the printing house who will check the correctness of uploaded files once again. This results in additional costs (not only financial) and unnecessarily extends the process of order fulfillment.</p>

<p>PreflightAPI was created to remedy these problems. Fully automatic and configurable checking process, available 24/7 and unit-settled allows you to reduce preflight costs while increasing efficiency (API does not go for coffee, does not call for a raise and does not complain about job).</p>

